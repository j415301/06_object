package com.obj.controller;

public class Animal {
	
	public String species;
	public String name;
	public int age;
	public String live;
	public int leg;
	public char gender;
	
	public Animal(String species, String name, int age, String live, int leg, char gender) {
		this.species = species;
		this.name = name;
		this.age = age;
		this.live = live;
		this.leg = leg;
		this.gender = gender;
	}

}
